def plot_abuse_defense_tradeoff(ax):
    """æ‚ªç”¨ã¨é˜²å¾¡ã®ãƒˆãƒ¬ãƒ¼ãƒ‰ã‚ªãƒ•"""
    transparency = np.linspace(0, 100, 100)
    
    # æ‚ªç”¨ã®ã—ã‚„ã™ã• (ç·šå½¢å¢—åŠ )
    abuse_ease = 30 + 0.5 * transparency
    
    # é˜²å¾¡ã®ã—ã‚„ã™ã• (Så­—ã‚«ãƒ¼ãƒ–)
    defense_ease = 20 + 70 / (1 + np.exp(-0.08 * (transparency - 50)))
    
    # ãƒãƒƒãƒˆå®‰å…¨æ€§ (é˜²å¾¡ - æ‚ªç”¨)
    net_safety = defense_ease - abuse_ease
    
    ax.plot(transparency, abuse_ease, 'r-', linewidth=3, label='Abuse Ease', alpha=0.7)
    ax.plot(transparency, defense_ease, 'g-', linewidth=3, label='Defense Ease', alpha=0.7)
    ax.plot(transparency, net_safety, 'b--', linewidth=3, label='Net Safety', alpha=0.7)
    
    # ç¾è¡ŒAIã¨SSD-LLM
    ax.axvline(x=20, color='orange', linestyle=':', linewidth=2, alpha=0.7)
    ax.text(20, 95, 'Current\nAI', ha='center', fontsize=9, color='orange', fontweight='bold')
    
    ax.axvline(x=85, color='purple', linestyle=':', linewidth=2, alpha=0.7)
    ax.text(85, 95, 'SSD-LLM', ha='center', fontsize=9, color='purple', fontweight='bold')
    
    # æœ€é©ç‚¹
    optimal_idx = np.argmax(net_safety)
    ax.plot(transparency[optimal_idx], net_safety[optimal_idx], 'b*', 
           markersize=20, label=f'Optimal ({transparency[optimal_idx]:.0f}%)')
    
    ax.axhline(y=0, color='black', linestyle='-', linewidth=1, alpha=0.3)
    ax.fill_between(transparency, 0, 100, where=(net_safety > 0), 
                    alpha=0.1, color='green', label='Safe Zone')
    ax.fill_between(transparency, 0, 100, where=(net_safety < 0), 
                    alpha=0.1, color='red', label='Danger Zone')
    
    ax.set_xlabel('Transparency (%)', fontsize=11, fontweight='bold')
    ax.set_ylabel('Score', fontsize=11, fontweight='bold')
    ax.set_title('Transparency Trade-off:\nAbuse vs Defense', fontsize=12, fontweight='bold')
    ax.legend(loc='best', fontsize=9)
    ax.grid(True, alpha=0.3)
    ax.set_xlim([0, 100])
    ax.set_ylim([0, 100])


