def plot_bias_analysis(ax):
    """ãƒã‚¤ã‚¢ã‚¹ã®åˆ†æ"""
    biases = ['Availability\nHeuristic', 'Confirmation\nBias', 
              'Negativity\nBias', 'Self-Criticism\nBias']
    impact = [25, 30, 20, 25]  # å„ãƒã‚¤ã‚¢ã‚¹ã®å½±éŸ¿åº¦
    colors = ['red', 'orange', 'yellow', 'pink']
    
    bars = ax.barh(biases, impact, color=colors, alpha=0.7, edgecolor='black', linewidth=2)
    
    # å€¤ã‚’è¡¨ç¤º
    for bar, val in zip(bars, impact):
        width = bar.get_width()
        ax.text(width + 1, bar.get_y() + bar.get_height()/2,
               f'+{val}%\nover-estimate', va='center', fontsize=9, fontweight='bold')
    
    # åˆè¨ˆ
    total_bias = sum(impact)
    ax.axvline(x=total_bias, color='red', linestyle='--', linewidth=3, alpha=0.7)
    ax.text(total_bias, 3.5, f'Total Bias:\n+{total_bias}%', ha='center', 
           fontsize=10, fontweight='bold', color='red',
           bbox=dict(boxstyle='round', facecolor='pink', alpha=0.5))
    
    ax.set_xlabel('Over-estimation (%)', fontsize=11, fontweight='bold')
    ax.set_title('Cognitive Biases in Initial Assessment', fontsize=12, fontweight='bold')
    ax.grid(True, alpha=0.3, axis='x')
    ax.set_xlim([0, 120])
    
    # æ³¨é‡ˆ
    ax.text(60, -0.7, 'Initial rating: 100/100\nRealistic rating: 30/100\nBias: ~70 points', 
           ha='center', fontsize=8, style='italic',
           bbox=dict(boxstyle='round', facecolor='wheat', alpha=0.7))


