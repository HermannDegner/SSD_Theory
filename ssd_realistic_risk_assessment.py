def plot_realistic_timeline(ax):
    """ã‚ˆã‚Šç¾å®Ÿçš„ãªã‚¿ã‚¤ãƒ ãƒ©ã‚¤ãƒ³"""
    years = np.arange(2025, 2036)
    
